package data;

public class Trailer extends People {
	private int hiba;
	private String ido;
	private String vido;

	public Trailer() {

	}

	public String getVido() {
		return vido;
	}

	public void setVido(String vido) {
		this.vido = vido;
	}

	public String getIdo() {
		return ido;
	}

	public void setIdo(String ido) {
		this.ido = ido;
	}

	public int getHiba() {
		return hiba;
	}

	public void setHiba(int hiba) {
		this.hiba = hiba;
	}
}
